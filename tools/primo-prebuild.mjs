#!/usr/bin/env node
// Primo-only prebuild: read primo-defaults.json (gitignored) and emit
// platform/mv3/extension/js/primo-defaults.js, which is consumed by
// primo-seed.js inside the extension.
//
// Run from the repo root via `node tools/primo-prebuild.mjs`.
// Invoked at the top of buildall.sh so any build path picks it up.

import { readFile, writeFile, access } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const CONFIG_PATH = path.join(REPO_ROOT, 'primo-defaults.json')
const OUT_PATH = path.join(
  REPO_ROOT, 'platform/mv3/extension/js/primo-defaults.js'
)

async function fileExists (p) {
  try { await access(p); return true } catch { return false }
}

function normaliseHostname (raw) {
  const s = String(raw ?? '').trim()
  if (!s) { return null }
  try {
    const url = new URL(/^[a-z]+:\/\//i.test(s) ? s : `https://${s}`)
    return url.hostname || null
  } catch {
    return s
  }
}

function normaliseLevel (raw, fallback) {
  const n = Number(raw)
  if (!Number.isInteger(n) || n < 0 || n > 3) {
    if (fallback === undefined) {
      throw new Error(`level must be an integer 0..3, got ${JSON.stringify(raw)}`)
    }
    return fallback
  }
  return n
}

async function main () {
  let config = { globalDefault: 0, sites: [] }
  if (await fileExists(CONFIG_PATH)) {
    let text
    try {
      text = await readFile(CONFIG_PATH, 'utf8')
    } catch (err) {
      throw new Error(`Failed reading ${path.basename(CONFIG_PATH)}: ${err.message}`)
    }
    let parsed
    try {
      parsed = JSON.parse(text.replace(/^﻿/, ''))
    } catch (err) {
      throw new Error(`Invalid JSON in ${path.basename(CONFIG_PATH)}: ${err.message}`)
    }
    if (!parsed || typeof parsed !== 'object') {
      throw new Error(`${path.basename(CONFIG_PATH)} must be a JSON object`)
    }
    config = parsed
  } else {
    console.warn(
      `⚠️  No ${path.basename(CONFIG_PATH)} found — using empty defaults ` +
      `(globalDefault=0, sites=[])`
    )
  }

  const globalDefault = normaliseLevel(config.globalDefault, 0)
  const seenHosts = new Set()
  const sites = []
  for (const entry of (Array.isArray(config.sites) ? config.sites : [])) {
    if (!entry || typeof entry !== 'object') { continue }
    const hostname = normaliseHostname(entry.hostname)
    if (!hostname) { continue }
    if (seenHosts.has(hostname)) { continue }
    const level = normaliseLevel(entry.level)
    seenHosts.add(hostname)
    sites.push({ hostname, level })
  }

  // Stable, deterministic version stamp so we can detect when the shipped
  // defaults change between builds. (FNV-1a 32-bit, hex.)
  const stamp = JSON.stringify({ g: globalDefault, s: sites })
  let h = 0x811c9dc5
  for (let i = 0; i < stamp.length; i++) {
    h ^= stamp.charCodeAt(i)
    h = Math.imul(h, 0x01000193) >>> 0
  }
  const version = h.toString(16).padStart(8, '0')

  const banner =
    `// AUTO-GENERATED by tools/primo-prebuild.mjs from primo-defaults.json.\n` +
    `// Do not edit; edit primo-defaults.json instead.\n`
  const body =
    `export const primoDefaultsVersion = ${JSON.stringify(version)};\n` +
    `export const primoGlobalDefault = ${globalDefault};\n` +
    `export const primoSiteOverrides = ${JSON.stringify(sites, null, 4)};\n`

  await writeFile(OUT_PATH, banner + body, 'utf8')
  console.log(
    `✅ Wrote ${path.relative(REPO_ROOT, OUT_PATH)} ` +
    `(globalDefault=${globalDefault}, ${sites.length} site override` +
    `${sites.length === 1 ? '' : 's'}, version=${version})`
  )
}

main().catch(err => {
  console.error('❌ primo-prebuild failed:', err.message || err)
  process.exit(1)
})
