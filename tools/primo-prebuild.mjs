#!/usr/bin/env node
// Primo-only prebuild: read PRIMO_DEFAULT_TRUSTED_SITES from .env / .env.local
// and emit platform/mv3/extension/js/primo-defaults.js. The output file is
// gitignored, .env.local is gitignored — the trusted-sites list is not
// committed. .env.local overrides .env when both define the same key.
//
// Run from the repo root via `node tools/primo-prebuild.mjs`.
// Invoked at the top of buildall.sh so any build path picks it up.

import { readFile, writeFile, access } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
// Order matters: later entries override earlier ones.
const ENV_FILES = ['.env', '.env.local'].map(f => path.join(REPO_ROOT, f))
const OUT_PATH = path.join(
  REPO_ROOT, 'platform/mv3/extension/js/primo-defaults.js'
)

// Tiny .env parser — KEY=VALUE per line, # comments, optional quotes around
// values. Avoids pulling in dotenv as a dep just for this.
function parseEnv (text) {
  const out = {}
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.trim()
    if (!line || line.startsWith('#')) { continue }
    const eq = line.indexOf('=')
    if (eq < 0) { continue }
    const key = line.slice(0, eq).trim()
    let value = line.slice(eq + 1).trim()
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    out[key] = value
  }
  return out
}

function parseSites (raw) {
  if (!raw) { return [] }
  const tokens = raw.split(/[,\s]+/).map(s => s.trim()).filter(Boolean)
  const out = []
  for (const token of tokens) {
    let host
    try {
      const url = new URL(/^[a-z]+:\/\//i.test(token) ? token : `https://${token}`)
      host = url.hostname
    } catch {
      host = token
    }
    if (host && !out.includes(host)) { out.push(host) }
  }
  return out
}

async function fileExists (p) {
  try { await access(p); return true } catch { return false }
}

async function main () {
  const env = {}
  let anyLoaded = false
  for (const file of ENV_FILES) {
    if (!(await fileExists(file))) { continue }
    Object.assign(env, parseEnv(await readFile(file, 'utf8')))
    anyLoaded = true
  }
  if (!anyLoaded) {
    console.warn('⚠️  No .env / .env.local found — generating empty trusted-sites list')
  }
  const sites = parseSites(env.PRIMO_DEFAULT_TRUSTED_SITES)

  const banner = `// AUTO-GENERATED by tools/primo-prebuild.mjs. Do not edit.\n` +
    `// Source: PRIMO_DEFAULT_TRUSTED_SITES in .env / .env.local\n`
  const body = `export const primoDefaultTrustedSites = ${
    JSON.stringify(sites, null, 4)
  };\n`

  await writeFile(OUT_PATH, banner + body, 'utf8')
  console.log(
    `✅ Wrote ${path.relative(REPO_ROOT, OUT_PATH)} ` +
    `(${sites.length} site${sites.length === 1 ? '' : 's'})`
  )
}

main().catch(err => {
  console.error('❌ primo-prebuild failed:', err.message || err)
  process.exit(1)
})
